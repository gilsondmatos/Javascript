// Operadores aritmeticos

//Função somar
function somarDoisNumeros(num1, num2) {
    return num1 + num2
}
// Função Subtrair
function subtrairDoisNumeros(num1, num2) {
    return num1 - num2
}
// Função Multiplicar
function multiDoisNumeros(num1, num2) {
    return num1 * num2
}
// Função Dividir
function dividirDoisNumeros(num1, num2) {
    return num1 / num2
}
// Função Resto da divisao
function restoDoisNumeros(num1, num2) {
    return num1 % num2
}
console.log(somarDoisNumeros(50,20))
console.log(subtrairDoisNumeros(50,20))
console.log(multiDoisNumeros(50,20))
console.log(dividirDoisNumeros(50,20))
console.log(restoDoisNumeros(50,20))

