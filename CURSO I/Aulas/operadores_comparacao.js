let num1 = 3

console.log(num1 > 0) // Maior 
console.log(num1 >= 0) // Maior ou igual
console.log(num1 < 0) // Menor
console.log(num1 <= 0) // Menor ou igual
console.log(num1 === 3) // identico
console.log(num1 !== 3) // Não identico

